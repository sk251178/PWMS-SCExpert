Create View repLocationStat
AS
SELECT 
LOCATION, 
CONVERT(NVARCHAR,STATUS) AS STATUS, 
PUTREGION ,
PICKREGION , 
WAREHOUSEAREA ,
LOCUSAGETYPE ,
LOCMHTYPE ,
CONVERT(NVARCHAR,INVENTORY) AS INVENTORY, 
LASTCOUNTDATE ,
ACCESSTYPE ,  
LENGTH ,
WIDTH ,
HEIGHT ,
CUBIC ,
LOADSCAPACITY ,
PENDINGWEIGHT ,
PENDINGCUBIC ,
PENDINGLOADS ,
CONVERT(NVARCHAR,LOOSEID) AS LOOSEID ,
AISLE ,
BAY ,
LOCLEVEL ,
HEIGHTFROMFLOOR,
LASTMOVEIN ,
LASTMOVEOUT ,
PICKSFROMLASTCOUNT 
FROM LOCATION